"""
Custom tool trajectory metrics using precision/recall on tool NAMES only.

Inspired by the codelab approach (trajectory_precision_func / trajectory_recall_func),
adapted for ADK's local AgentEvaluator interface.

These metrics replace the strict exact-matching tool_trajectory_avg_score for cases where:
  - The agent reformulates tool arguments (e.g., 'laptops' vs 'Laptops under $1000')
  - The agent takes extra steps (e.g., searches then reviews, expected only reviews → F1=0.67)
  - Sub-agent delegation changes the top-level call (update expected tool names in dataset)

Metric: tool_name_f1
  precision = |actual_names ∩ expected_names| / |actual_names|
  recall    = |actual_names ∩ expected_names| / |expected_names|
  f1        = 2 * P * R / (P + R)

  Argument values are IGNORED — only the tool name is compared.

Legacy / fallback: the original exact-match metric is still available via
  tests/eval_configs/unit/standard_exact.json  (tool_trajectory_avg_score: 0.5)
"""

from __future__ import annotations

from typing import Optional


def _get_tool_names(invocation) -> set[str]:
    """Extract tool names from an invocation's intermediate_data.

    Handles both storage formats:
      - tool_uses: simplified format used in some eval datasets (e.g. demo_user_002)
      - invocation_events: detailed event format used by actual agent runs and
        datasets generated by generate_eval_dataset.py (e.g. product_agent_direct)

    Returns the union of tool names found in either format.
    """
    if invocation is None:
        return set()
    intermediate = getattr(invocation, "intermediate_data", None)
    if intermediate is None:
        return set()

    names: set[str] = set()

    # Format 1: tool_uses (simple, e.g. demo_user_002.test.json)
    tool_uses = getattr(intermediate, "tool_uses", None) or []
    for tu in tool_uses:
        name = getattr(tu, "name", None)
        if name:
            names.add(name)

    # Format 2: invocation_events (detailed, used by actual agent runs and
    # datasets produced by generate_eval_dataset.py)
    invocation_events = getattr(intermediate, "invocation_events", None) or []
    for event in invocation_events:
        content = getattr(event, "content", None)
        if content is None:
            continue
        parts = getattr(content, "parts", None) or []
        for part in parts:
            fc = getattr(part, "function_call", None)
            if fc is not None:
                name = getattr(fc, "name", None)
                if name:
                    names.add(name)

    return names


def _f1(actual: set[str], expected: set[str]) -> float:
    """Compute F1 score between actual and expected tool name sets."""
    if not expected:
        # No tools expected: full score if agent also called none
        return 1.0 if not actual else 0.5
    if not actual:
        # Tools expected but none called
        return 0.0
    inter = actual & expected
    precision = len(inter) / len(actual)
    recall = len(inter) / len(expected)
    if precision + recall == 0:
        return 0.0
    return 2 * precision * recall / (precision + recall)


def tool_name_f1(
    eval_metric,
    actual_invocations: list,
    expected_invocations: Optional[list] = None,
    conversation_scenario=None,
):
    """
    Tool trajectory F1 metric based on tool NAMES only (ignores argument values).

    ADK custom metric interface:
        eval_metric          -- EvalMetric with criterion.threshold
        actual_invocations   -- list[Invocation] from agent run
        expected_invocations -- list[Invocation] from eval dataset
        conversation_scenario -- unused

    Returns an EvaluationResult compatible with ADK's AgentEvaluator.

    Scoring per invocation:
        F1 = 2 * precision * recall / (precision + recall)
        precision = |{actual tool names} ∩ {expected tool names}| / |{actual tool names}|
        recall    = |{actual tool names} ∩ {expected tool names}| / |{expected tool names}|

    Example scores:
        search_products called, search_products expected          → F1 = 1.0
        [search, reviews] called, [reviews] expected              → F1 = 0.67 (passes at 0.5)
        no tools called (clarification), [search] expected        → F1 = 0.0  (fails)
        order_agent called, order_agent expected (after update)   → F1 = 1.0
    """
    from google.adk.evaluation.evaluator import EvalStatus, EvaluationResult, PerInvocationResult

    threshold = 0.5
    if eval_metric is not None:
        criterion = getattr(eval_metric, "criterion", None)
        if criterion is not None:
            threshold = getattr(criterion, "threshold", 0.5)

    per_invocation_results = []
    scores = []
    expected_list = expected_invocations or []

    for i, actual_inv in enumerate(actual_invocations):
        expected_inv = expected_list[i] if i < len(expected_list) else None
        actual_names = _get_tool_names(actual_inv)
        expected_names = _get_tool_names(expected_inv)

        score = _f1(actual_names, expected_names)
        scores.append(score)

        status = EvalStatus.PASSED if score >= threshold else EvalStatus.FAILED
        per_invocation_results.append(
            PerInvocationResult(
                actual_invocation=actual_inv,
                expected_invocation=expected_inv,
                score=score,
                eval_status=status,
            )
        )

    overall = sum(scores) / len(scores) if scores else 0.0
    overall_status = EvalStatus.PASSED if overall >= threshold else EvalStatus.FAILED

    return EvaluationResult(
        overall_score=overall,
        overall_eval_status=overall_status,
        per_invocation_results=per_invocation_results,
    )
